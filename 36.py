def isValidSudoku(board: List[List[str]]) -> bool:
    n = 9
    rows = [set() for _ in range(n)]
    cols = [set() for _ in range(n)]
    boxs = [set() for _ in range(n)]
    for r in range(n):
        for c in range(n):
            x = board[r][c]
            if x == ".":
                continue
            if x in rows[r]:
                return False
            rows[r].add(x)
            if x in cols[c]:
                return False
            cols[c].add(x)
            i = (r//3)*3+c//3
            if x in boxs[i]:
                return False
            boxs[i].add(x)
    return True

